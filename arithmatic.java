import java.lang.String;
public class arithmatic {
    public static void main(String[] args){
        int x=6,y=5;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(x++);
        System.out.println(y++);
        System.out.println(x--);
        System.out.println(y--);


    }
}
